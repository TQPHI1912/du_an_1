<script type="text/javascript">
const face = document.querySelector('.counters.faces h2')
const youtube = document.querySelector('.counters.youtubes h2')
const tiktok = document.querySelector('.counters.tiktoks h2')
const inta = document.querySelector('.counters.intas h2')

function counterUp(el, to) {
    let speed = 200
    let from = 0
    let step = to / speed
    const counter = setInterval(function() {
        from += step
        if (from > to) {
            clearInterval(counter)
            el.innerText = to
        } else {
            el.innerText = Math.ceil(from)
        }
    }, 1)
}
window.addEventListener("scroll", function() {
    const scrollHeight = window.pageYOffset;
    //const navHeight = navbar.getBoundingClientRect().height;
    console.log('scroll height: ', scrollHeight)
    //console.log('nav height: ', navHeight)
    if (scrollHeight > 700 && scrollHeight < 900) {
        counterUp(face, 33)
        counterUp(youtube, 10)
        counterUp(tiktok, 990)
        counterUp(inta, 256)
        // navbar.classList.add("fixed-nav");

    }

});

counterUp(face, 2434)
